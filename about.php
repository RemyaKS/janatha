            <h2>TRIBAL PEOPLE IN INDIA</h2><br><br>
            <div id="paragraph">
            <span>T</span>ribal peoples constitute 8.6 percent of India’s total population, about 104 million people according to the 2011 census (68 million people according to the 1991 census). This is the largest population of the tribal people in the world. One concentration lives in a belt along the Himalayas stretching through Jammu and Kashmir, Himachal Pradesh, and Uttar Pradesh in the west, to Assam, Meghalaya, Tripura, Arunachal Pradesh, Mizoram, Manipur, and Nagaland in the northeast. Another concentration lives in the hilly areas of central India (Madhya Pradesh, Orissa, and, to a lesser extent, Andhra Pradesh); in this belt, which is bounded by the Narmada River to the north and the Godavari River to the southeast, tribal peoples occupy the slopes of the region's mountains. Other tribals, the Santals, live in Bihar and West Bengal. There are smaller numbers of tribal people in Karnataka, Tamil Nadu, and Kerala, in western India in Gujarat and Rajasthan, and in the union territories of Lakshadweep and the Andaman and Nicobar Islands. 

            The extent to which a state's population is tribal varies considerably. In the northeastern states of Arunachal Pradesh, Meghalaya, Mizoram, and Nagaland, upward of 90 percent of the population is tribal. However, in the remaining northeast states of Assam, Manipur, Sikkim, and Tripura, tribal peoples form between 20 and 30 percent of the population. The largest tribes are found in central India, although the tribal population there accounts for only around 10 percent of the region's total population. Major concentrations of tribal people live in Maharashtra, Orissa, and West Bengal. In the south, about 1 percent of the populations of Kerala and Tamil Nadu are tribal, whereas about 6 percent in Andhra Pradesh and Karnataka are members of tribes. 


            Tribal people in India are called “adivasi”. Adivasi is an umbrella term for a heterogeneous set of ethnic and tribal groups considered the aboriginal population of India. Although terms such as atavika, vanavasi ("forest dwellers"), or girijan ("hill people") are also used for the tribes of India, adivasi carries the specific meaning of being the original and autochthonous inhabitants of a given region and was specifically coined for that purpose in the 1930s. Over time, unlike the terms "aborigines" or "tribes", the word "adivasi" has developed a connotation of past autonomy which was disrupted during the British colonial period in India and has not been restored.[8]They generally live outside the mainstream of Indian Hindu and Muslim society. Most ordinary Indians known little about them.

            There are some 573 communities recognized by the government as Scheduled Tribes and therefore eligible to receive special benefits and to compete for reserved seats in legislatures and schools. They range in size from the Gonds (roughly 7.4 million) and the Santals (approximately 4.2 million) to only eighteen Chaimals in the Andaman Islands. Central Indian states have the country's largest tribes, and, taken as a whole, roughly 75 percent of the total tribal population live there.
            In the 1950s a policy of protection was adopted towards all the tribal peoples in India. Prime Minister Jawaharlal Nehru once wrote: "There is no point in trying to make them a second rate copy of ourselves...they are people who sing and dance and try to enjoy life; not people who sit in stock exchanges, shout at each other, and think themselves civilized." Book: “The Tribals of India”, a photo-essay by Sunil Janah.
